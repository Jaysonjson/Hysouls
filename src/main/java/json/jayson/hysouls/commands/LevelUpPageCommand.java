package json.jayson.hysouls.commands;

import com.hypixel.hytale.component.Ref;
import com.hypixel.hytale.component.Store;
import com.hypixel.hytale.server.core.command.system.CommandContext;
import com.hypixel.hytale.server.core.command.system.basecommands.AbstractPlayerCommand;
import com.hypixel.hytale.server.core.command.system.basecommands.CommandBase;
import com.hypixel.hytale.server.core.entity.entities.Player;
import com.hypixel.hytale.server.core.universe.PlayerRef;
import com.hypixel.hytale.server.core.universe.world.World;
import com.hypixel.hytale.server.core.universe.world.storage.EntityStore;
import json.jayson.hysouls.ui.LevelPage;
import org.jetbrains.annotations.NotNull;

public class LevelUpPageCommand extends AbstractPlayerCommand {
    public LevelUpPageCommand() {
        super("lvl_up", "placeholder");
    }

    @Override
    protected void execute(@NotNull CommandContext commandContext, @NotNull Store<EntityStore> store, @NotNull Ref<EntityStore> ref, @NotNull PlayerRef playerRef, @NotNull World world) {
        store.getComponent(ref, Player.getComponentType()).getPageManager().openCustomPage(ref, store, new LevelPage(playerRef));
    }
}
